///////////////////////////////////////////////////////////////////////////////////////////////////
///Общие параметры для создания физического контроллера
///////////////////////////////////////////////////////////////////////////////////////////////////
struct ControllerDesc
{
  bound_volumes::aaboxf world_bound_box;     //ограничивающий объём физического мира
  const char*           configuration_name;  //путь к файлу, содержащему описание конфигурации контроллера
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///Базовый класс физического тела
///////////////////////////////////////////////////////////////////////////////////////////////////
class Body: public Entity
{
  public:
    typedef xtl::com_ptr<Body>       Pointer;
    typedef xtl::com_ptr<const Body> ConstPointer;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Создание тела
///////////////////////////////////////////////////////////////////////////////////////////////////
    static Pointer Create ();

///////////////////////////////////////////////////////////////////////////////////////////////////
///Установка физической модели
///////////////////////////////////////////////////////////////////////////////////////////////////
    void        SetModelName (const char* name);
    const char* ModelName    () const;
    
  private:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Конструктор / деструктор
///////////////////////////////////////////////////////////////////////////////////////////////////
    Body  ();
    ~Body ();    

///////////////////////////////////////////////////////////////////////////////////////////////////
///Метод, вызываемый при посещении данного объекта
///////////////////////////////////////////////////////////////////////////////////////////////////
    void AcceptCore (Visitor&);

  private:
    struct Impl;
    stl::auto_ptr<Impl> impl;
};

