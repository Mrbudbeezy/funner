///////////////////////////////////////////////////////////////////////////////////////////////////
///URL opening handler
///////////////////////////////////////////////////////////////////////////////////////////////////
class IOpenUrlHandler
{
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Destructor
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual ~IOpenUrlHandler () {};

///////////////////////////////////////////////////////////////////////////////////////////////////
///Handle URL opening
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual void HandleUrlOpen (const char* url) = 0;
};


///////////////////////////////////////////////////////////////////////////////////////////////////
///Application launcher
///////////////////////////////////////////////////////////////////////////////////////////////////
class Application
{
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Constructor / destructor
///////////////////////////////////////////////////////////////////////////////////////////////////
    Application ();
    ~Application ();

///////////////////////////////////////////////////////////////////////////////////////////////////
///Main loop start
///////////////////////////////////////////////////////////////////////////////////////////////////
    void Run (engine::IEngine* engine);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Set open URL handler
///////////////////////////////////////////////////////////////////////////////////////////////////
    void SetOpenUrlHandler (IOpenUrlHandler* handler);

  private:
    Application (const Application&);             //no impl
    Application& operator = (const Application&); //no impl

  private:
    struct Impl;
    Impl* impl;
};

