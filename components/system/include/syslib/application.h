#ifndef SYSLIB_APPLICATION_HEADER
#define SYSLIB_APPLICATION_HEADER

#include <cstddef>

#include <stl/string_fwd>
#include <xtl/functional_fwd>

#include <syslib/application_delegate.h>

namespace common
{

//forward declaration
class PropertyMap;

}

namespace syslib
{

///////////////////////////////////////////////////////////////////////////////////////////////////
///События приложения
///////////////////////////////////////////////////////////////////////////////////////////////////
enum ApplicationEvent
{
  ApplicationEvent_OnExit,          //получен сигнал завершения приложения
  ApplicationEvent_OnIdle,          //событие обработки пользовательских очередей сообщений
  ApplicationEvent_OnRunLoopEnter,  //событие входа в очередь обработки сообщений
  ApplicationEvent_OnRunLoopExit,   //событие выхода из очереди обработки сообщений
  ApplicationEvent_OnInitialize,    //событие иницализации приложения
  ApplicationEvent_OnPause,         //приложение приостановлено
  ApplicationEvent_OnResume,        //приложение восстановлено
  ApplicationEvent_OnStop,          //приложение остановлено
  ApplicationEvent_OnStart,         //приложение восстановлено
  ApplicationEvent_OnMemoryWarning, //приложение получило предупреждение об использовании большого объема памяти

  ApplicationEvent_Num
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///Режим работы в фоне
///////////////////////////////////////////////////////////////////////////////////////////////////
enum ApplicationBackgroundMode
{
  ApplicationBackgroundMode_Active,  //приложение активно в фоне
  ApplicationBackgroundMode_Suspend, //приложения не активно в фоне
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///Приложение
///////////////////////////////////////////////////////////////////////////////////////////////////
class Application
{
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Установка делегата приложения
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void BeginDelegate (IApplicationDelegate*);
    static void EndDelegate   ();

///////////////////////////////////////////////////////////////////////////////////////////////////
///Запуск обработки очереди сообщений
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void Run (IApplicationDelegate* delegate = 0);
    static bool IsMessageLoop ();

///////////////////////////////////////////////////////////////////////////////////////////////////
///Обработка сообщений нити
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void ProcessThreadMessages ();

///////////////////////////////////////////////////////////////////////////////////////////////////
///Прекращение обработки очереди сообщений
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void Exit        (int code);
    static int  GetExitCode ();  //получение кода завершения

///////////////////////////////////////////////////////////////////////////////////////////////////
///Пауза на заданное количество милисекунд
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void Sleep (size_t milliseconds);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Открытие URL во внешнем браузере
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void OpenUrl (const char* url);
    
///////////////////////////////////////////////////////////////////////////////////////////////////
///Получение значения переменной окружения
///////////////////////////////////////////////////////////////////////////////////////////////////
    static stl::string GetEnvironmentVariable (const char* name);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Подписка на события приложения
///////////////////////////////////////////////////////////////////////////////////////////////////
    typedef xtl::function<void ()> EventHandler;

    static xtl::connection RegisterEventHandler (ApplicationEvent event, const EventHandler& handler);
    
///////////////////////////////////////////////////////////////////////////////////////////////////
///Подписка на сообщения / посылка сообщений
///////////////////////////////////////////////////////////////////////////////////////////////////
    typedef xtl::function<void (const char*)> NotificationHandler;

    static xtl::connection RegisterNotificationHandler (const char* notification_wildcard, const NotificationHandler& handler);
    static void            PostNotification            (const char* notification);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Управление энергосбережением
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void SetScreenSaverState (bool state);
    static void EnableScreenSaver   () { SetScreenSaverState (true); }
    static void DisableScreenSaver  () { SetScreenSaverState (false); }    
    static bool ScreenSaverState    ();

///////////////////////////////////////////////////////////////////////////////////////////////////
///Управление режимом работы в фоне
///////////////////////////////////////////////////////////////////////////////////////////////////
    static void                      SetBackgroundMode (ApplicationBackgroundMode mode);
    static ApplicationBackgroundMode BackgroundMode    ();
    
///////////////////////////////////////////////////////////////////////////////////////////////////
///Получение платформо-зависимых системных свойств
///////////////////////////////////////////////////////////////////////////////////////////////////
    static common::PropertyMap SystemProperties     ();
    static void                SetSystemProperty    (const char* name, const char* value);
    static void                RemoveSystemProperty (const char* name);
};

}

#endif
