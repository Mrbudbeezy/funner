#ifndef RENDER_LOW_LEVEL_OBJECT_HEADER
#define RENDER_LOW_LEVEL_OBJECT_HEADER

namespace render
{

namespace low_level
{

//forward declaration
class IDevice;
class IPropertyList;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Базовый класс семейства низкоуровневых классов отрисовки
///////////////////////////////////////////////////////////////////////////////////////////////////
class IObject
{
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Подсчёт ссылок
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual void AddRef  () = 0;
    virtual void Release () = 0;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Получение списка свойств объекта
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual IPropertyList* GetProperties () = 0;

  protected:
    virtual ~IObject () {}
    
  private:
    IObject& operator = (const IObject&); //no impl
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///Объект, созданный устройством отрисовки
///////////////////////////////////////////////////////////////////////////////////////////////////
class IDeviceObject: virtual public IObject
{
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Указатель на устройство отрисовки, которому принадлежит ресурс
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual IDevice* GetDevice () = 0;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Проверка совместимости объеков
///////////////////////////////////////////////////////////////////////////////////////////////////
    virtual bool IsCompatible (IObject*) = 0; //убрать!!!???
};

}

}

#endif
