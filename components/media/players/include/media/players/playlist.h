#ifndef MEDIALIB_PLAYER_PLAY_LIST_HEADER
#define MEDIALIB_PLAYER_PLAY_LIST_HEADER

#include <cstddef>

namespace media
{

namespace players
{

///////////////////////////////////////////////////////////////////////////////////////////////////
///Список проигрывания
///////////////////////////////////////////////////////////////////////////////////////////////////
class Playlist
{
  public:
///////////////////////////////////////////////////////////////////////////////////////////////////
///Конструкторы / деструктор / присваивание
///////////////////////////////////////////////////////////////////////////////////////////////////
    Playlist  ();
    Playlist  (const Playlist&);
    ~Playlist ();
    
    Playlist& operator = (const Playlist&);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Идентификатор списка
///////////////////////////////////////////////////////////////////////////////////////////////////
    size_t Id () const;
    
///////////////////////////////////////////////////////////////////////////////////////////////////
///Копирование
///////////////////////////////////////////////////////////////////////////////////////////////////
    Playlist Clone () const;
    
///////////////////////////////////////////////////////////////////////////////////////////////////
///Количество источников / проверка на пустоту
///////////////////////////////////////////////////////////////////////////////////////////////////
    unsigned int Size    () const;
    bool         IsEmpty () const;
    
///////////////////////////////////////////////////////////////////////////////////////////////////
///Получение источников
///////////////////////////////////////////////////////////////////////////////////////////////////
    const char** Items () const;
    const char*  Item  (unsigned int index) const;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Добавление и удаление источников
///////////////////////////////////////////////////////////////////////////////////////////////////
    unsigned int AddSource    (const char* source_name);
    void         RemoveSource (unsigned int source_index);
    void         RemoveSource (const char* source_name); //all sources with source_name
    void         Clear        ();
    
///////////////////////////////////////////////////////////////////////////////////////////////////
///Резервирование памяти
///////////////////////////////////////////////////////////////////////////////////////////////////
    void         Reserve  (unsigned int count);
    unsigned int Capacity () const;

///////////////////////////////////////////////////////////////////////////////////////////////////
///Обмен
///////////////////////////////////////////////////////////////////////////////////////////////////
    void Swap (Playlist&);

  private:
    struct Impl;
    Impl* impl;
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///Обмен
///////////////////////////////////////////////////////////////////////////////////////////////////
void swap (Playlist&, Playlist&);

}

}

#endif
