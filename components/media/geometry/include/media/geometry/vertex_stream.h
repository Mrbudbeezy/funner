///////////////////////////////////////////////////////////////////////////////////////////////////
    const VertexWeight* Data () const;
          VertexWeight* Data ();

///////////////////////////////////////////////////////////////////////////////////////////////////
///Количество вершин
///////////////////////////////////////////////////////////////////////////////////////////////////
    size_t Size   () const;
    void   Resize (size_t weights_count);

///////////////////////////////////////////////////////////////////////////////////////////////////
///Очистка массива
///////////////////////////////////////////////////////////////////////////////////////////////////
