enum CloneMode
{
  CloneMode_Copy,     //копирование
  CloneMode_Instance, //инстанцирование (увеличение числа ссылок)
};

///////////////////////////////////////////////////////////////////////////////////////////////////
/// опирование
///////////////////////////////////////////////////////////////////////////////////////////////////
template <class T> inline T clone (T& resource, CloneMode mode = CloneMode_Copy)
{
  return T (resource, mode);
}

